async function onPrevBtnClick(image) {
    now = now - 1;
    zoom = false;
    await loadImage(Object.values(imgList[now])[0]); // 이미지 로드 기다림
    image.setAttribute("src", Object.values(imgList[now])[0]); 
    setImgBtn(image);
  }
  
  /**이미지 오른쪽 버튼 클릭 이벤트 */
  async function onNextBtnClick(image) {
    now = now + 1;
    zoom = false;
    await loadImage(Object.values(imgList[now])[0]); // 이미지 로드 기다림
    image.setAttribute("src", Object.values(imgList[now])[0]);
    setImgBtn(image);
  }
  

